/**
 * Ковариантность: направление отношения тип - подтип сохраняется
 *
 * Ковариантный класс - это обобщенный класс (в качестве примера будем использовать Producer<T>), для которого верно
 * следующее:
 *      Producer<A> этo подтип Producer<B>, если А - подтип В.
 * Мы называем это "сохранением направления отношения тип-подтип".
 *
 * Объявление типового параметра класса ковариантным разрешает передавать значения этого класса в аргументах и
 * возвращать их из функций, когда типовой аргумент неточно соответствует типу параметра в определении функции.
 *
 * Не каждый класс можно объявить ковариантным: это может быть небезопасно.
 * Объявление класса ковариантным по определенному типовому параметру ограничивает возможные способы использования этого
 * параметра в классе. Чтобы гарантировать безопасность типов, он может использоваться только в так называемых
 * исходящих (оиt) позициях: то есть класс может производить значения типа T, но не потреблять их.
 *
 * Использование типового параметра в объявлениях членов класса можно разделить на:
 *      -> входящие (in)
 *          Если T используется как тип параметра функции, он находится во входящей позиции.
 *          Такая функция потребляет значения типа T.
 *      -> исходящие (оиt)
 *          Если Т используется как тип возвращаемого значения функции, то он находится в исходящей позиции.
 *          В этом случае функция производит значения типа T.
 * позиции.
 */
