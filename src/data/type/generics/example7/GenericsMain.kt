// MutableList<T> не получится объявить ковариантным по типовому параметру, потому что он включает методы, принимающие
// значения типа T в виде параметров и возвращающие такие значения (то есть тип T присутствует в обеих позициях).
interface MutableList<T>: List<T>, MutableCollection<T> {
    override fun add(element: T): Boolean
}

