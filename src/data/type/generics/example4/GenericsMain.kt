/**
 * Использование приведения с обобщенным типом
 *
 * Этот код прекрасно скомпилируется: компилятор выведет предупреждение, но признает этот код допустимым.
 */
fun printSum1(c : Collection<*>) {
    val intList = c as? List<Int>           // Этот код породит предупреждение: "Unchecked cast: List<*> to List<lnt>"
        ?: throw IllegalArgumentException("List is expected")

    println(intList.sum())
}

