package data.type.generics.example4

/**
 * Обобщенные типы реализуются в JVM через механизм стирания типа (type erasure) - то есть типовые аргументы
 * экземпляров обобщенных классов не сохраняются во время выполнения.
 *
 * Чтобы типовые аргументы не стирались (или, говоря терминами языка Kotlin, овеществлялись), функцию можно объявить
 * встраиваемой.
 *
 * Если создать список List<String> и добавить в него несколько строк, во время выполнения вы увидите, что этот список
 * имеет тип List - нет никакой возможности определить тип элементов списка.
 *
 */
fun main() {
    // Хотя компилятор видит два различных типа, во время выполнения списки выглядят совершенно одинаковыми.
    val list1: List<String> = listOf("а", "b")
    val list2: List<Int> = listOf(1, 2, 3)

    // Все работает, как ожидается
    print("1. ")
    printSum1(listOf(1, 2, 3))
    // Множество - не список, поэтому возбуждается ислюючение
    /*print("2. ")
    printSum1(setOf(1, 2, 3))*/
    // Приведение выполнено успешно, но потом возбуждается другое исключение
    /*print("3. ")
    printSum1(listOf("а", "b", "c"))*/

    print("4. ")
    printSum2(listOf(1, 2, 3))
}

